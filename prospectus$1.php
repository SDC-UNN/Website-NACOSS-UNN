            <h3 >Course Description</h3>            
            <div>
                <h5>COS 101 INTRODUCTION TO COMPUTER SCIENCE (2 Units)</h5>
                <p>
                    <span lang=EN-US>History of Computers Functional Components characteristics
                        of a computer. Problem solving; flowcharts, algorithms. Computer programming
                        statements, symbolic names, arrays, subscripts, expressions; introduction to BASIC or FORTRAN
                        language. ( This course is a required ancillary for
                        many faculties. In exceptional cases, where explicitly specified by NUC,
                        specified versions of the course be offered).
                    </span>
                </p>

                <h5>COS 102: INTRODUCTION TO COMPUTER SYSTEM (2 Units)</h5>
                <p><span lang=EN-US>Computer structure; organization and
                        function of the control unit, arithmetic –logic unit, memory unit of the CPU as
                        well as the registers used by the CPU; relationship amongst the CPU, memory,
                        and peripheral devices. Number Systems: conversion of (binary, octal, decimal,
                        hexadecimal); binary arithmetic; weighted and non- weighted binary codes.
                        Introduction to machine language; data and instruction formats.</span></p>

                <h5>COS 104: COMPUTER PRACTICE (2 Units)</h5>
                <p><span lang=EN-US>Interactions of the user with the operating
                        systems through command language or job control language. Functions and types
                        of interpretative routines, linkage editors, and loaders. A lot of simple
                        programming problems should be given to allow students have good practices with
                        the language introduction in COS 101.</span></p>

