            <h3 >Course Description</h3>            
            <div>
                <h5>COS 201: COMPUTER PROGRAMMING I (2 Units)</h5>
                <p><span lang=EN-US>Introduction to problem solving methods and
                        algorithm development: designing, coding, debugging and documenting problems
                        using techniques of good programming style. A widely used programming language
                        should be used in teaching these course, structured form or FORTRAIN or Pascal.</span></p>

                <p><span lang=EN-US>(This course is required ancillary for many
                        faculties. In exceptional cases, where explicitly specified by NUC, specified
                        versions of the course be offered).</span></p>

                <h5>COS 202: COMPUTER PROGRAMMING II (2 Units)</h5>
                <p><span lang=EN-US>Principals of
                        good programming structured programming concept; debugging and testing; sting
                        processing. Internal searching and sorting; data structures; recursion. </span></p>
                <p ><span lang=EN-US>A programming
                        language different from that used in COS 201 should be used here. Pascal, PL/I
                        or COBOL is Recommended.</span></p>

                <h5 >COS 221: INTRODUCTION TO FILE PROCESSING (2 Units)</h5>
                <p ><span lang=EN-US>Introduction to
                        data management files; job control language application An over- view of I/O
                        (Input/Output) architecture; logic file organization; mapping logical
                        organization; into physical storage; back- up procedure, file recovery; higher
                        language management.</span></p>

                <h5>COS 251: ASSEMBLY LANGUAGE PROGRAMMING (2 Units)</h5>
                <p ><span lang=EN-US>Basic
                        concepts and general formats of assembly language instructions; micro/macro
                        instruction formats; segmentation and linkage; transfer of control. The
                        assembly language of an available machine should be taught so that students can
                        run their assembly language programs.</span></p>
