            <h3 >Course Description</h3>      
            <div>
                <h5 >COS 301: INTRODUCTION TO DIGITAL COMPUTER (2 Units)</h5>
                <p ><span lang=EN-US>Boolean algebra, gates and gating devices, combinational logic, sequential logic,
                        microprocessor. </span></p>

                <h5 >COS 303: INTRODUCTION TO MICROCOMPUTERS SYSTEM (2Units)</h5>
                <p ><span lang=EN-US>Functional description; microprocessor architecture; systems operation and interfacing;
                        types of instructions and instruction formats. (A specific microcomputer-based
                        design may be used to teach this course).</span></p>

                <h5 >COS 304: COMPUTER APPLICATIONS (2 UNITS)</h5>
                <p ><span lang=EN-US>Prerequisite: COS 101 and/or COS 201</span></p>
                <p ><span lang=EN-US>Use of computers in various sectoral applications in a modern economy. (This course is
                        a required ancillary for some facilities but students of Computer Science
                        Department and those doing a combined degree in Computer Science are not
                        allowed to take this course).</span></p>

                <h5 >COS 311: NUMERICAL METHODS I (2 Units )</h5>
                <p ><span lang=EN-US>Interpolation;Â  approximation; numerical integration and differentiation; solutions of non-linear equations;
                        solutions of ordinary differential equations; introduction to partial
                        differential equations.</span></p>

                <h5 >COS 313 : SWITCHING ALGEBRA &amp; DISCRETE STRUCTURES I (2 Units)</h5>
                <p ><span lang=EN-US>Boolean Algebra. Combinational and sequential networks.
                        Directed graphs; cycles, connectivity. State transition graphs.</span></p>

                <h5 >COS 314: SWITCHING ALGEBRA DISCRETE STRUCTURES II (2 Units)</h5>
                <p ><span lang=EN-US>Finite state machines. Algebraic system; groups, homomorphism, congruence.
                        Applications to machine structures. Correctness proof for a simple computer.</span></p>

                <h5 >COS 315: OPERATIONS RESEARCH I (2 units)</h5>
                <p ><span lang=EN-US>The nature of Operations Research. Linear Programming Problems; formulation,
                        simplex method; transportation problems; simplex method for transportation
                        problems.</span></p>

                <h5 >COS 316: AUTOMATA THEORY &amp; FORMAL LANGUAGES (2 Units)</h5>
                <p ><span lang=EN-US>Formal
                        grammars and automata; Regular languages; context-free languages; deterministic
                        parsing of context-free languages; recursive languages.</span></p>

                <h5 >COS 321: DATABASE DESIGN AND MANAGEMENT I (2 Units)</h5>
                <p ><span lang=EN-US>Data base management systems; review of basic concepts; functions and components of
                        DBMS. File design and access path; future directions in DBMS; programming and
                        applications in D-base environment.</span></p>

                <h5 >COS 322: DATABASE DESIGN AND MANAGEMENT II (2 Units)</h5>
                <p ><span lang=EN-US>In this second part, a specific Data Base should be used e.g Dbase V or higher, Oracle,
                        etc,</span></p>

                <h5 >COS 331: COMPILER CONSTRUCTION I ( 2 Units)</h5>
                <p ><span lang=EN-US>Review of compilers, assemblers and interpreters; structure and functional aspect of a
                        compiler; syntax, semantics and pragmatics; functional and code generation.
                        Internal representation of a program. Use of a standard compiler as a working
                        vehicle ( FORTRAN, COBOL OR PL/1). Error detection and recovery. Grammar and
                        languages; The parsing problem. The scanner.</span></p>

                <h5>COS 333: OPERATING SYSTEMS I ( 2 Units) </h5>
                <p ><span lang=EN-US>Introduction
                        to processes; processor management; memory management; multiprogramming
                        concepts and requirements; multi-processing systems; other related issues based
                        on semaphore and monitor concepts.</span></p>

                <h5>COS 334: OPERATING SYSTEMS II (2 Units) </h5>
                <p ><span lang=EN-US>Issues
                        in analyzing and designing operating systems; memory management; protection;
                        resource allocation.</span></p>

                <h5>COS 335: SYSTEMS ANALYSIS AND DESIGN ( 2 Units) </h5>
                <p ><span lang=EN-US>Investigation
                        of the working of existing systems; analysis of results; design of a new system
                        to improve the existing ones; communication of the new system to all others;
                        maintenance.</span></p>

                <h5>COS 341: COMPUTER ARCHITECTURE I ( 2 Units) </h5>
                <p ><span lang=EN-US>Basic
                        logic design; data representations; instruction formats; basic computer
                        organization and design study of the architecture of an actual simple
                        minicomputer.</span></p>

                <h5>COS 342: COMPUTER ARCHITECTURE II (2 Units) </h5>
                <p ><span lang=EN-US>Memory
                        system; general characteristics of memory operation, (Technology-magnetic
                        recording, semiconductor memory, charged coupled devices, magnetic bubble);
                        memory addressing, memory control systems, hardware control, microprogrammed
                        control, asynchronous control, I/O control; introduction to the methodology of
                        fault-tolerance computing. </span></p>

                <h5>COS 352: DATA STRUCTURES ( 2 Units) </h5>
                <p ><span lang=EN-US>Bits,
                        bytes, words; linear structures and list structures arrays, tree structures;
                        sets and relations; Higher-level language data types and data handling
                        facilities.</span></p>

                <h5>COS 372: LABORATORY FOR DIGITAL DESIGN ( 3 Units) </h5>
                <p ><span lang=EN-US>Practical
                        designs and operation of the laboratory equipment.</span></p>

                <h5>COS 374: INDUSTRIAL WORKING EXPERIENCE ( 4 Units) </h5>
                <p ><span lang=EN-US>Three
                        months industrial attachment during the long vacation between the end of third
                        year and the beginning of fourth year. A comprehensive report by the student,
                        must be submitted to his/her supervisor as soon as the fourth year begins.</span></p>

